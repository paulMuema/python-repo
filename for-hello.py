print("Using a for loop")

for i in range(1, 6):
    print(i, "Hello")

print("Using a while loop")

j = 1 # initialize the loop variable
while j <= 5:
    print(j, "Hello")
    # the loop variable must be updated
    # in each iteration until the condition
    # j <= 5 become false
    j = j + 1 


